/*main function to compute and return best DFS path from one city to another*/

find_dfs(Destination, Destination, _, [Destination]).
find_dfs(Source, Destination, Visited, [Source|Path]) :-
    city_next(Source, City_next, Visited),
    find_dfs(City_next,Destination,[City_next|Visited],Path).


/*To find the next city in the path to be taken - must not be visited before*/

city_next(Current,Next,Path) :-
    route(Current,Next, _),
    not(member(Next,Path)).

